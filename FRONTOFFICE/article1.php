La perception des clients de la solution idéale
Même si les problématiques des clients sont très concrètes, les besoins naissent dans leur esprit sous forme de projection vers une situation idéale. Les clients assemblent ces éléments (souci + espoir + idéal) à leur façon, sous forme d’une demande– pas toujours formalisée en cahier des charges.</p>

<p>Généralement, dans ce processus de prise de conscience nait une idée de solution. Le client (se) dit : « il me faut … ».</p>

<p>À ce stade, une image va venir en surimpression dans l’esprit des clients : l’image qu’ils ont de votre produit ou service par rapport à leur idée de solution. Pour que l’histoire se termine bien pour votre solution, il est vital de comprendre comment les clients/prospects vous perçoivent et en quoi cette perception vous est favorable. La perception prime dans la décision : c’est ce qu’ils vont ressentir à votre sujet qui l’emportera au final.</p>

<p>Leur image à votre sujet est subjective : elle provient d’expériences passées, de bouche -à-oreille, d’informations recueillies sur internet, et de ce qu’ils ont compris de votre communication de marque.</p>



<h2>5 questions à se poser pour conquérir l’esprit du client</h2>

<ol><li>Définir la problématique, c’est-à-dire ce qui pose souci aux clients : savez-vous quelles questions se posent vos clients ?</li>
<li>Profiler les clients : pouvez-vous faire des regroupements de clients qui partagent une même problématique ? (ils ont besoin de … et attendent …, tout en souhaitant que … )</li>
<li>Évaluer la demande : Combien y a-t-il clients (prospects) par profil ?</li>
<li>Visualiser leur perception : Quelle est l’image du profil à votre sujet ? Que disent-ils à votre sujet par rapport à ce qu’il leur faut ?</li>
<li>Implanter votre image : quelle action pourrait façonner/modifier l’image qu’ils ont dans la tête à votre sujet ?<li></ol>

Points de vigilance :

<ul>
<li>Toujours s’assurer que le besoin exprimé par un de vos clients correspond à une demande marché</li>
<li>Les clients savent ce qu’ils veulent (besoin et attentes) mais pas forcément ce qu’ils souhaitent en plus</li>
<li>Profiler les clients signifie aussi savoir comment/où/quand les contacter et communiquer avec eux</li>
<li>À la limite, il vaut mieux que les clients n’aient pas d’image en tête plutôt qu’une image négative de votre produit ou service. </li>
</ul>

<h2>Exercez-vous avec un cas concret</h2>
<p>Le service formation d’un fabricant français de machines-outils, qui emploie 2 000 personnes, a identifié une problématique prégnante autour des questions que se posent les salariés au sujet de leur évolution de carrière à partir de 20 ans d’ancienneté dans l’entreprise.</p>

<p>Après enquête, un profil prioritaire ressort. Il s’agit de tous les collaborateurs qui ont besoin de progresser en management, qui attendent que le service formation leur propose un apprentissage des techniques d’animation d’équipe et souhaitent, sans y dédier plus d’un jour par semaine, être opérationnelles rapidement pour être capables de postuler un nouveau poste avec plus de responsabilités d’encadrement.</p>

<p>Les résultats de la quantification du nombre des employés qui, d’après leurs entretiens individuels, sont dans ce cas, a révélé qu’il y a 150 personnes, qui partagent cette problématique. Leur profil est généralement masculin, bac +3 technique, dans les filiales de production de l’entreprise. L’image qu’elles ont de l’offre du service formation est plutôt floue. Elles craignent qu’une formation soit comme de retourner à l’école. Selon, elles, les formations en management s’adressent à ceux qui sont déjà managers !</p>
